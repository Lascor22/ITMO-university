Реализуем веб сервис, который будет общаться с базой данных. Реализуем GUI, 
который будет общаться с веб сервисом.
Для первого этапа требуется получить FlightId. Его можно выбрать в виде 
списка, который берется из таблицы Flights. Далее пользователь выберет 
необходимый рейс, после чего мы выведем все свободные места для выбранного рейса.
